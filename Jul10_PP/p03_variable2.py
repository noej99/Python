# -*- coding:utf-8 -*-

# Java : byte/short/int/long
#        -> 적당한 사이즈 골라서
#        -> 효율적인 프로그램
# Python : int
#        -> 개발 편하게
a = 10
print(a, type(a), id(a))

# Java : float/double
# Python : float
b = 3.141592
print(b, type(b), id(b))

# Java : char(기본형, 한글자)/String(참조형, 여러글자)
# Python : str(", ' 구별없음)
c = "ㅋ"
print(c, type(c), id(c))

# Java : boolean
# Python : bool
d = True #False
print(d, type(d), id(d))
